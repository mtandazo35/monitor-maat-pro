def register_payment(
    user_id: int,
    amount: float,
    days: int,
    currency: str = "USD",
    method: Optional[str] = None,
    notes: Optional[str] = None,
    registered_by: Optional[dict] = None,
) -> dict:
    user = get_user(user_id)
    if not user:
        raise UserError("Usuario no encontrado.")
    if user["role"] == "admin":
        raise UserError("Los administradores no requieren pagos.")
    if days <= 0:
        raise UserError("Los días deben ser mayores a 0.")
    if amount < 0:
        raise UserError("El monto no puede ser negativo.")

    today = today_local()
    current = None
    if user.get("paid_until"):
        try:
            current = date.fromisoformat(user["paid_until"])
        except ValueError:
            current = None
    base = current if current and current > today else today
    new_until = base + timedelta(days=days)

    with connect() as con:
        cur = con.execute(
            """INSERT INTO payments
               (user_id, amount, currency, days, method, notes,
                paid_at, registered_by_id, registered_by_username, covers_until)
               VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?)""",
            (
                user_id, float(amount), (currency or "USD").strip().upper(),
                int(days),
                (method or "").strip() or None,
                (notes or "").strip() or None,
                now_iso(),
                registered_by["id"] if registered_by else None,
                registered_by["username"] if registered_by else None,
                new_until.isoformat(),
            ),
        )
        pid = cur.lastrowid
        con.execute(
            "UPDATE users SET paid_until = ?, payment_warning_sent_for = NULL WHERE id = ?",
            (new_until.isoformat(), user_id),
        )
        row = con.execute("SELECT * FROM payments WHERE id = ?", (pid,)).fetchone()
    return dict(row)


