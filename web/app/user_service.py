def list_due_users(within_days: int = 3) -> list[dict]:
    """Devuelve usuarios 'user' cuyo paid_until está dentro del próximo `within_days`,
    o ya vencido hace ≤1 día. Excluye admin."""
    today = today_local()
    until = (today + timedelta(days=within_days)).isoformat()
    yesterday = (today - timedelta(days=1)).isoformat()
    with connect() as con:
        rows = con.execute(
            """SELECT * FROM users
               WHERE role = 'user'
                 AND paid_until IS NOT NULL
                 AND paid_until >= ?
                 AND paid_until <= ?""",
            (yesterday, until),
        ).fetchall()
    return [dict(r) for r in rows]


